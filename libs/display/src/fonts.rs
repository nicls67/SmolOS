#[derive(Copy, Clone)]
pub enum FontSize {
    Font12,
    Font16,
    Font20,
    Font24,
}

impl FontSize {
    pub(crate) fn is_pixel_set(&self, ascii_char: u8, x: u8, y: u8) -> bool {
        match self {
            FontSize::Font12 => {
                let col_data = FONT_ASCII_12
                    [(ascii_char - 0x20) as usize * self.get_char_size().1 as usize + y as usize];
                col_data & (1 << (7 - x)) != 0
            }
            FontSize::Font16 => {
                let col_data = FONT_ASCII_16
                    [(ascii_char - 0x20) as usize * self.get_char_size().1 as usize + y as usize];
                col_data & (1 << (15 - x)) != 0
            }
            FontSize::Font20 => {
                let col_data = FONT_ASCII_20
                    [(ascii_char - 0x20) as usize * self.get_char_size().1 as usize + y as usize];
                col_data & (1 << (15 - x)) != 0
            }
            FontSize::Font24 => {
                let col_data = FONT_ASCII_24
                    [(ascii_char - 0x20) as usize * self.get_char_size().1 as usize + y as usize];
                col_data & (1 << (31 - x)) != 0
            }
        }
    }

    pub(crate) fn get_char_size(&self) -> (u8, u8) {
        match self {
            FontSize::Font12 => (7, 12),
            FontSize::Font16 => (11, 16),
            FontSize::Font20 => (14, 20),
            FontSize::Font24 => (17, 24),
        }
    }
}
